// 3: List and describe the five primitive data types in JavaScript. 

// 1Number: This data type represents numeric values, including integers and floating-point numbers. For example:

let age = 25;
let pi = 3.14;

// String: Strings represent sequences of characters enclosed in single or double quotes. They're used for text data.

let greeting = 'Hello, World!';
let name = "Alice";

// Boolean: Booleans have only two possible values: true or false. They're commonly used for logical operations and conditional statements.


let isLogged = true;
let hasPermission = false

// Null: This primitive type has only one value: null. It represents the intentional absence of any object value.


let data = null;

// Undefined: When a variable is declared but not assigned a value, it is of type undefined. It means the variable has been declared but has not yet been assigned any value.

let undefinedVar;

